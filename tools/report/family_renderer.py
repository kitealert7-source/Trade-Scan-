def _render_verdicts(d: dict) -> list[str]:
    variants = d.get("variants", [])
    if not variants:
        return []
    out = ["## 16. Deployment Verdict", "",
           "Canonical verdict via `filter_strategies._compute_candidate_status`.",
           "Soft-gate trips (advisory) may demote a CORE/WATCH variant to FAIL (effective).",
           ""]
    for v in variants:
        vd = v.get("verdict") or {}
        effective = vd.get("effective_status", vd.get("status", "—"))
        canonical = vd.get("status", "—")
        rationale = vd.get("rationale", "")
        if effective != canonical:
            header = f"### {_short_name(v['directive_id'])} — **{effective}** (canonical: {canonical})"
        else:
            header = f"### {_short_name(v['directive_id'])} — **{effective}**"
        out.append(header)
        out.append("")
        out.append(rationale)
        out.append("")
    out.append("---")
    out.append("")
    return out


