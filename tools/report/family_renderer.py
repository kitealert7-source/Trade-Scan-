def _render_direction(d: dict) -> list[str]:
    variants = d.get("variants", [])
    if not variants:
        return []
    out = ["## 9. Direction Concentration", "",
           "Share of POSITIVE PnL by direction.",
           "",
           "| Variant | Long share | Short share | Verdict |",
           "|---------|-----------|-------------|---------|"]
    for v in variants:
        ds = v.get("direction_share", {}) or {}
        long_s = _f(ds.get("long"))
        short_s = _f(ds.get("short"))
        if max(long_s, short_s) > 85.0:
            verdict = "⚠ Extreme bias"
        elif min(long_s, short_s) >= 30.0:
            verdict = "Balanced"
        else:
            verdict = "Tilted"
        out.append(
            f"| {_short_name(v['directive_id'])} | "
            f"{long_s:.1f}% | {short_s:.1f}% | {verdict} |"
        )
    out.append("")
    out.append("---")
    out.append("")
    return out


def _render_regime_cells(d: dict) -> list[str]:
    variants = d.get("variants", [])
    if not variants:
        return []
    out = ["## 10. Regime Cell Matrix", ""]
    out.append("Best and worst Direction×{Session, Trend, Volatility} cells (min 30 trades).")
    out.append("")
    for axis_label, axis_key in [("Session", "session"),
                                  ("Trend",   "trend"),
                                  ("Vol",     "volatility")]:
        out.append(f"### 10.{axis_label[0]}. Direction × {axis_label}")
        out.append("")
        out.append("| Variant | Best cell | Best PnL | Worst cell | Worst PnL |")
        out.append("|---------|-----------|----------|------------|-----------|")
        for v in variants:
            cells = (v.get("regime_cells", {}) or {}).get(axis_key, {}) or {}
            best = cells.get("best")
            worst = cells.get("worst")
            if best is None and worst is None:
                out.append(f"| {_short_name(v['directive_id'])} | — | — | — | — |")
                continue
            b_lbl = best["cell"] if best else "—"
            b_pnl = f"${best['net_pnl']:+,.2f}" if best else "—"
            w_lbl = worst["cell"] if worst else "—"
            w_pnl = f"${worst['net_pnl']:+,.2f}" if worst else "—"
            out.append(
                f"| {_short_name(v['directive_id'])} | "
                f"{b_lbl} | {b_pnl} | {w_lbl} | {w_pnl} |"
            )
        out.append("")
    out.append("---")
    out.append("")
    return out


def _render_yearwise(d: dict) -> list[str]:
    variants = d.get("variants", [])
    if not variants:
        return []
    out = ["## 11. Yearwise Stability", "",
           "Per-year PnL % of variant net. Flag: any year > 60% = dominant.",
           ""]
    # Union of years across variants
    all_years = sorted({y for v in variants for y in (v.get("yearwise") or {}).keys()})
    if not all_years:
        out.append("_No yearwise data available._")
        out.append("")
        out.append("---")
        out.append("")
        return out
    out.append("| Variant | " + " | ".join(all_years) + " | Max year % |")
    out.append("|---------|" + "|".join(["----" for _ in all_years]) + "|-----------|")
    for v in variants:
        yw = v.get("yearwise") or {}
        total = float(sum((row or {}).get("net_pnl", 0.0) for row in yw.values())) or 1.0
        cells: list[str] = []
        shares: list[float] = []
        for y in all_years:
            row = yw.get(y)
            if row is None:
                cells.append("—")
                shares.append(0.0)
                continue
            pnl = float(row.get("net_pnl", 0.0))
            share = pnl / total * 100.0
            shares.append(abs(share))
            cells.append(f"${pnl:,.0f} ({share:+.0f}%)")
        max_share = max(shares) if shares else 0.0
        flag = " ⚠" if max_share > 60.0 else ""
        out.append(f"| {_short_name(v['directive_id'])} | " + " | ".join(cells) + f" | {max_share:.0f}%{flag} |")
    out.append("")
    out.append("---")
    out.append("")
    return out


