"""Markdown renderer for the Family Analysis Report.

Renders a single markdown document from per-variant pre-computed analytics.
The orchestrator (`tools/family_report.py`) is responsible for gathering data
and computing per-variant metrics; this module is rendering-only.

Wrapper-first per FAMILY_REPORT_IMPLEMENTATION_PLAN.md Rule 4: this module
performs no analytics — all numbers come pre-computed in the `family_data`
dict from the orchestrator.

Section order (matches `outputs/family_psbrk_24m_report.md`):
  1. Executive ranking
  2. Core metrics + Δ vs parent
  3. Lineage (parent → child diff)
  4. Structural attribution
       4a. Concentration / tail
       4b. Direction × Session / Trend / Volatility cell matrices
       4c. Yearwise stability
       4d. Streaks
       4e. Rolling stability
       4f. Early/late
       4g. Drawdown clusters
  5. Per-variant verdict
"""

from __future__ import annotations

from datetime import datetime, timezone
from typing import Any


def render(family_data: dict) -> str:
    """Top-level: return the full markdown report for `family_data`."""
    lines: list[str] = []
    lines.extend(_render_header(family_data))
    lines.extend(_render_dedup_notice(family_data))
    lines.extend(_render_window_warnings(family_data))
    lines.extend(_render_executive_ranking(family_data))
    lines.extend(_render_promotion_summary(family_data))
    lines.extend(_render_core_metrics(family_data))
    lines.extend(_render_parent_delta(family_data))
    lines.extend(_render_prior_run_delta(family_data))
    lines.extend(_render_lineage(family_data))
    lines.extend(_render_concentration(family_data))
    lines.extend(_render_session(family_data))
    lines.extend(_render_direction(family_data))
    lines.extend(_render_regime_cells(family_data))
    lines.extend(_render_yearwise(family_data))
    lines.extend(_render_streaks(family_data))
    lines.extend(_render_rolling(family_data))
    lines.extend(_render_early_late(family_data))
    lines.extend(_render_drawdown(family_data))
    lines.extend(_render_verdicts(family_data))
    lines.append("")
    lines.append(f"*Generated by `tools/family_report.py` at {family_data.get('generated_at', '')}*")
    return "\n".join(lines) + "\n"


