def _short_name(name: str) -> str:
    """Return the trailing `_S0n_Vm_Pkk` portion when prefix is shared.

    Renders compact in tables. Caller decides how aggressive to short.
    """
    parts = name.split("_")
    # Heuristic: prefix is the first 5 tokens (idea_FAMILY_SYMBOL_TF_MODEL)
    if len(parts) >= 8:
        return "_".join(parts[5:])
    return name


def _tail_verdict(top5_share: float, body_after_top20: float | None) -> str:
    if top5_share > 0.70:
        return "⚠ SEVERELY TAIL-DEPENDENT"
    if body_after_top20 is not None and body_after_top20 < -500.0:
        return "⚠ TAIL-DEPENDENT"
    if top5_share > 0.40:
        return "TAIL-LEANING"
    return "BODY-DRIVEN"


def _f(v) -> float:
    try:
        if v is None:
            return 0.0
        return float(v)
    except Exception:
        return 0.0
