_FX_CORR_TIMEFRAMES = ("1h", "4h")


# ---------------------------------------------------------------------------
# USD_SYNTH factor catalog (S12 generalization, 2026-05-16)
# ---------------------------------------------------------------------------
# Maps column name -> source file under SYSTEM_FACTORS/USD_SYNTH/. Used by
# load_usd_synth_factor to resolve which file holds the requested column.
# Each file contains multiple period variants of the same metric family.

_USD_SYNTH_FACTOR_CATALOG = {
    # Compression family — path_length / net_displacement ratio.
    # LOW = trending; HIGH = chop. Use operator '>=' to gate "skip when below threshold".
    "compression_5d":  "usd_synth_compression_d1.csv",
    "compression_20d": "usd_synth_compression_d1.csv",
    # Volatility family — rolling realized vol (annualized).
    # HIGH = turbulent/trending; LOW = quiet/chop. Use operator '<=' to gate "skip when above threshold".
    "vol_5d":  "usd_synth_volatility_d1.csv",
    "vol_20d": "usd_synth_volatility_d1.csv",
    "vol_60d": "usd_synth_volatility_d1.csv",
    # Persistence family — rolling lag-1 autocorr of returns.
    # HIGH = trending (momentum); LOW/negative = mean-reverting. Use operator '<=' to gate "skip when above threshold".
    "autocorr_5d":  "usd_synth_persistence_d1.csv",
    "autocorr_20d": "usd_synth_persistence_d1.csv",
    "autocorr_60d": "usd_synth_persistence_d1.csv",
    # Stretch family — Z-score of returns vs rolling window.
    # HIGH abs = extreme move; LOW abs = normal. Z is signed; gating semantics are caller-specific.
    "stretch_z20": "usd_synth_stretch_d1.csv",
    "stretch_z60": "usd_synth_stretch_d1.csv",
}


# ---------------------------------------------------------------------------
# USD-conversion reference pairs (2026-05-15) — for H2_recycle@3 cross-pair
# PnL support. Each non-USD currency maps to a USD-anchored pair whose close
# is used to convert that currency to USD at runtime.
# ---------------------------------------------------------------------------

_CCY_TO_USD_PAIR = {
    "EUR": "EURUSD",
    "GBP": "GBPUSD",
    "AUD": "AUDUSD",
    "NZD": "NZDUSD",
    "JPY": "USDJPY",
    "CHF": "USDCHF",
    "CAD": "USDCAD",
}


def _split_pair_ccys(symbol: str) -> tuple[str, str]:
    """Return (base, quote) currency 3-letter codes."""
    if len(symbol) != 6:
        return ("", "")
    return symbol[:3].upper(), symbol[3:].upper()


def _required_ref_pairs(trade_symbols: list[str]) -> list[str]:
    """Return USD-anchored pairs needed as reference for currency conversion.

    Excludes trade symbols themselves (those would self-reference).
    """
    needed: set[str] = set()
    for sym in trade_symbols:
        base, quote = _split_pair_ccys(sym)
        for ccy in (base, quote):
            if ccy and ccy != "USD" and ccy in _CCY_TO_USD_PAIR:
                ref = _CCY_TO_USD_PAIR[ccy]
                if ref not in trade_symbols:
                    needed.add(ref)
    return sorted(needed)


# ---------------------------------------------------------------------------
# Phase 5b.4 — process-local year-file cache.
#
# Multi-window matrix runs (e.g., h2_parity_run.py --all) often re-load
# the same year-files across windows (window A 2016-2018 and window E
# 2017-2019 both need 2017 + 2018). Caching at the year-file granularity
# means each year-file is parsed once per process. Cleared by
# clear_year_file_cache() if needed (e.g., between independent matrix
# runs in the same long-running session).
#
# Sized for ~10 years × ~10 symbols = 100 unique frames; an OctaFx 5m
# year-file is ~5-7 MB CSV → ~50-100 MB pandas memory. 64-entry cap
# keeps total RAM under 6 GB even in the worst case.
# ---------------------------------------------------------------------------


@lru_cache(maxsize=64)
def _read_year_csv_cached(symbol: str, year: int, timeframe: str = "5m") -> pd.DataFrame:
    """Parse one year of OCTAFX bars for one symbol at the given timeframe.
    Cached process-locally (per (symbol, year, timeframe)).

    Returned DataFrame is the FULL year (no window filter applied) with
    `time` as a DatetimeIndex. Callers slice by date range themselves.
    Returns an EMPTY DataFrame if the year-file is missing — callers
    should treat that as a gap year.

    `timeframe` selects the OCTAFX CSV file (5m, 15m, 30m, 1h available
    per the 2026-05 ingest). Default "5m" preserves legacy behavior for
    all pre-2026-05-19 callers.
    """
    research_dir = DATA_ROOT / "MASTER_DATA" / f"{symbol}_OCTAFX_MASTER" / "RESEARCH"
    f = research_dir / f"{symbol}_OCTAFX_{timeframe}_{year}_RESEARCH.csv"
    if not f.is_file():
        return pd.DataFrame()  # caller treats as gap year
    df = pd.read_csv(f, comment="#", parse_dates=["time"])
    df = df.set_index("time").sort_index()
    return df


def clear_year_file_cache() -> None:
    """Drop all cached year-files. Call between independent multi-window
    runs in the same process if memory matters."""
    _read_year_csv_cached.cache_clear()


